class D2POModel(nn.Module):
    def __init__(self, model_name, model_type="vlm"):
        """
        Initialize D²PO model
        
        Args:
            model_name: Name of the base LVLM (Qwen2-VL, LLaVA, etc.)
            model_type: Either "vlm" for vision-language model or "lm" for language model
        """
        super(D2POModel, self).__init__()
        
        self.model_type = model_type
        self.model = AutoModelForCausalLM.from_pretrained(model_name)
        self.tokenizer = AutoTokenizer.from_pretrained(model_name)
        
    def encode_image(self, image_path):
        """Process image input according to model type"""
        if self.model_type == "vlm":
            # For VLMs, we would use the model's image processor
            # This is simplified as each model has its own way of handling images
            image = Image.open(image_path).convert("RGB")
            # This is a placeholder for the actual image processing
            return self.model.process_image(image)
        else:
            # For LMs, we might use image captions or ignore images
            return None
    
    def forward(self, input_ids, attention_mask, labels=None, images=None):
        """Forward pass for the model"""
        inputs = {
            "input_ids": input_ids,
            "attention_mask": attention_mask,
        }
        
        if images is not None and self.model_type == "vlm":
            inputs["images"] = images
            
        if labels is not None:
            inputs["labels"] = labels
            
        return self.model(**inputs)
    
    def generate_action(self, state_image, state_desc, goal, previous_actions, previous_observations):
        """Generate next action given the current state"""
        # Construct prompt
        prompt = f"Goal: {goal}\n"
        
        for i, (action, obs) in enumerate(zip(previous_actions, previous_observations)):
            prompt += f"Step {i+1}: {action} -> {obs}\n"
            
        prompt += f"Current state: {state_desc}\nGenerate the next action:"
        
        # Tokenize
        inputs = self.tokenizer(prompt, return_tensors="pt").to(self.model.device)
        
        # Add image if VLM
        if self.model_type == "vlm" and state_image is not None:
            image_tensor = self.encode_image(state_image)
            inputs["images"] = image_tensor.to(self.model.device)
        
        # Generate
        output_ids = self.model.generate(
            **inputs,
            max_new_tokens=100,
            temperature=0.8,
            do_sample=True
        )
        
        # Decode
        generated_text = self.tokenizer.decode(output_ids[0], skip_special_tokens=True)
        action_text = generated_text[len(prompt):].strip()
        parts = action_text.split("Action:")
        reasoning = parts[0].replace("Reasoning:", "").strip() if len(parts) > 1 else ""
        action = parts[1].strip() if len(parts) > 1 else action_text
        
        return {
            "action": action,
            "reasoning": reasoning
        }
    
    def predict_next_state(self, state_image, state_desc, action):
        """Predict the next state after taking an action"""
        # Construct prompt
        prompt = f"Current state: {state_desc}\nAction: {action}\nPredict the next state:"
        
        # Tokenize
        inputs = self.tokenizer(prompt, return_tensors="pt").to(self.model.device)
        
        # Add image if VLM
        if self.model_type == "vlm" and state_image is not None:
            image_tensor = self.encode_image(state_image)
            inputs["images"] = image_tensor.to(self.model.device)
        
        # Generate
        output_ids = self.model.generate(
            **inputs,
            max_new_tokens=100,
            temperature=0.2,  # Lower temperature for state prediction
        )
        
        # Decode
        generated_text = self.tokenizer.decode(output_ids[0], skip_special_tokens=True)
        next_state = generated_text[len(prompt):].strip()
        
        return next_state

class ActionPreferenceDataset(Dataset):
    def __init__(self, preference_pairs, tokenizer, max_length=512):
        self.preference_pairs = preference_pairs
        self.tokenizer = tokenizer
        self.max_length = max_length
        
    def __len__(self):
        return len(self.preference_pairs)
    
    def __getitem__(self, idx):
        pair = self.preference_pairs[idx]
        
        # Construct prompt for chosen action
        chosen_prompt = f"Goal: {pair.goal}\n"
        
        for i, (action, obs) in enumerate(zip(pair.prev_actions, pair.prev_observations)):
            chosen_prompt += f"Step {i+1}: {action} -> {obs}\n"
            
        chosen_prompt += f"Reasoning: {pair.chosen_reasoning}\nAction: {pair.chosen_action}"
        
        # Construct prompt for rejected action
        rejected_prompt = f"Goal: {pair.goal}\n"
        
        for i, (action, obs) in enumerate(zip(pair.prev_actions, pair.prev_observations)):
            rejected_prompt += f"Step {i+1}: {action} -> {obs}\n"
            
        rejected_prompt += f"Reasoning: {pair.rejected_reasoning}\nAction: {pair.rejected_action}"
        
        # Tokenize
        chosen_encodings = self.tokenizer(
            chosen_prompt, 
            max_length=self.max_length, 
            padding="max_length", 
            truncation=True,
            return_tensors="pt"
        )
        
        rejected_encodings = self.tokenizer(
            rejected_prompt, 
            max_length=self.max_length, 
            padding="max_length", 
            truncation=True,
            return_tensors="pt"
        )
        
        return {
            "chosen_input_ids": chosen_encodings.input_ids.squeeze(),
            "chosen_attention_mask": chosen_encodings.attention_mask.squeeze(),
            "rejected_input_ids": rejected_encodings.input_ids.squeeze(),
            "rejected_attention_mask": rejected_encodings.attention_mask.squeeze(),
        }

