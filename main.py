@dataclass
class TrajectoryStep:
    state_image: str  # Path to image file
    state_desc: str   # Text description of state
    action: str       # Action taken
    object: str       # Object acted upon
    reasoning: str    # Reasoning behind action
    success: bool     # Whether action execution succeeded
    
@dataclass
class Trajectory:
    goal: str
    steps: List[TrajectoryStep]
    success: bool  # Whether trajectory reached the goal
    
@dataclass
class ActionPreferencePair:
    goal: str
    prev_actions: List[str]
    prev_observations: List[str]
    chosen_reasoning: str
    chosen_action: str
    rejected_reasoning: str
    rejected_action: str
    
@dataclass
class StatePreferencePair:
    current_state_image: str
    current_state_desc: str
    action: str
    chosen_next_state: str
    rejected_next_state: str

