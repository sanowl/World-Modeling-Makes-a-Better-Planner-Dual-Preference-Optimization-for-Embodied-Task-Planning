class TreeSearchNode:
    def __init__(self, state_image, state_desc, action=None, parent=None):
        self.state_image = state_image
        self.state_desc = state_desc
        self.action = action  # Action that led to this state
        self.parent = parent
        self.children = []
        self.score = 0.0
        
    def add_child(self, state_image, state_desc, action, score):
        child = TreeSearchNode(state_image, state_desc, action, self)
        child.score = score
        self.children.append(child)
        return child
    
    def get_path(self):
        """Get path from root to this node"""
        path = []
        current = self
        while current.parent is not None:
            path.append(current)
            current = current.parent
        path.append(current)  # Add root
        return list(reversed(path))

class TreeSearch:
    def __init__(self, env, policy_model, process_reward_model, threshold=3.75, alpha=0.5, max_depth=25, max_children=5):
        """
        Initialize the tree search
        
        Args:
            env: Environment simulator
            policy_model: Model for sampling actions
            process_reward_model: Model for evaluating actions (GPT-4o in the paper)
            threshold: Minimum score for action to be selected for expansion
            alpha: Weight for process reward vs environmental feasibility
            max_depth: Maximum tree depth
            max_children: Maximum number of children to explore per node
        """
        self.env = env
        self.policy_model = policy_model
        self.process_reward_model = process_reward_model
        self.threshold = threshold
        self.alpha = alpha
        self.max_depth = max_depth
        self.max_children = max_children
        
    def sample_actions(self, state_image, state_desc, goal, previous_actions, num_samples=5):
        """Sample potential actions from policy model"""
        # This would interface with your policy model to generate actions
        # For simplicity, we'll return dummy actions
        actions = [
            {"action": "find", "object": "cup", "reasoning": "Need to locate the cup first"},
            {"action": "pickup", "object": "cup", "reasoning": "Need to pick up the cup before using it"},
            # ... more actions would be generated by the model
        ]
        return actions[:num_samples]
    
    def evaluate_action(self, state_image, state_desc, goal, action, previous_actions):
        """Evaluate an action using both process reward and environmental feasibility"""
        # Process reward from GPT-4o (scale 0-5)
        proc_reward = self.process_reward_model.evaluate(
            state_image, state_desc, goal, action, previous_actions
        )
        
        # Environmental feasibility from simulator (0 or 1)
        env_feasibility = self.env.check_action_feasibility(state_image, action)
        
        # Combined score
        total_score = self.alpha * proc_reward + (1 - self.alpha) * env_feasibility
        
        return {
            "proc_reward": proc_reward,
            "env_feasibility": env_feasibility,
            "total_score": total_score
        }
    
    def execute_action(self, state_image, action):
        """Execute action in the environment and get next state"""
        success, next_state_image, next_state_desc = self.env.step(state_image, action)
        return success, next_state_image, next_state_desc
    
    def check_goal_reached(self, state_image, state_desc, goal):
        """Check if the goal has been reached"""
        return self.env.check_goal_completion(state_image, state_desc, goal)
    
    def search(self, initial_state_image, initial_state_desc, goal):
        """
        Perform tree search to find a successful trajectory
        
        Args:
            initial_state_image: Path to initial state image
            initial_state_desc: Description of initial state
            goal: Goal instruction
            
        Returns:
            List of successful trajectories
        """
        root = TreeSearchNode(initial_state_image, initial_state_desc)
        queue = [(root, 0)]  # (node, depth)
        successful_trajectories = []
        
        while queue:
            node, depth = queue.pop(0)
            
            # Check if we've reached max depth
            if depth >= self.max_depth:
                continue
                
            # Check if goal is reached
            if self.check_goal_reached(node.state_image, node.state_desc, goal):
                path = node.get_path()
                trajectory = self.construct_trajectory(path, goal)
                successful_trajectories.append(trajectory)
                continue
            
            # Get path so far for context
            path_so_far = node.get_path()
            previous_actions = [n.action for n in path_so_far if n.action is not None]
            
            # Sample potential actions
            potential_actions = self.sample_actions(
                node.state_image, 
                node.state_desc, 
                goal, 
                previous_actions,
                self.max_children
            )
            
            # Evaluate and filter actions
            for action_info in potential_actions:
                action = action_info["action"] + " " + action_info["object"]
                reasoning = action_info["reasoning"]
                
                # Evaluate action
                evaluation = self.evaluate_action(
                    node.state_image, 
                    node.state_desc, 
                    goal, 
                    action, 
                    previous_actions
                )
                
                # Only expand if score is above threshold
                if evaluation["total_score"] >= self.threshold:
                    # Execute action in the environment
                    success, next_state_image, next_state_desc = self.execute_action(
                        node.state_image, action
                    )
                    
                    if success:
                        # Create child node
                        action_with_reasoning = {
                            "action": action,
                            "reasoning": reasoning
                        }
                        child = node.add_child(
                            next_state_image, 
                            next_state_desc, 
                            action_with_reasoning,
                            evaluation["total_score"]
                        )
                        
                        # Add to queue for breadth-first exploration
                        queue.append((child, depth + 1))
        
        return successful_trajectories
    
    def construct_trajectory(self, path, goal):
        """Convert a path of nodes to a trajectory"""
        steps = []
        
        for i in range(1, len(path)):
            prev_node = path[i-1]
            node = path[i]
            
            action_info = node.action
            
            step = TrajectoryStep(
                state_image=prev_node.state_image,
                state_desc=prev_node.state_desc,
                action=action_info["action"].split()[0],
                object=action_info["action"].split()[1] if len(action_info["action"].split()) > 1 else "",
                reasoning=action_info["reasoning"],
                success=True  # Since these are steps from successful execution
            )
            steps.append(step)
        
        return Trajectory(goal=goal, steps=steps, success=True)

class PreferenceDataGenerator:
    def __init__(self, tree_search, gpt4o_state_describer):
        self.tree_search = tree_search
        self.gpt4o_state_describer = gpt4o_state_describer
        
    def generate_action_preference_pairs(self, successful_trajectory, all_sampled_actions):
        """
        Generate action preference pairs from a successful trajectory
        
        Args:
            successful_trajectory: A successful trajectory
            all_sampled_actions: Dictionary mapping (state_image, step) to all sampled actions at that step
            
        Returns:
            List of action preference pairs
        """
        pairs = []
        goal = successful_trajectory.goal
        
        for i, step in enumerate(successful_trajectory.steps):
            # Get history so far
            prev_steps = successful_trajectory.steps[:i]
            prev_actions = [s.action + " " + s.object for s in prev_steps]
            prev_observations = [s.state_desc for s in prev_steps]
            
            # Chosen action
            chosen_action = step.action + " " + step.object
            chosen_reasoning = step.reasoning
            
            # Get rejected actions for this state
            state_key = (step.state_image, i)
            if state_key in all_sampled_actions:
                rejected_actions = [
                    a for a in all_sampled_actions[state_key] 
                    if a["action"] + " " + a["object"] != chosen_action
                ]
                
                for rejected in rejected_actions:
                    rejected_action = rejected["action"] + " " + rejected["object"]
                    rejected_reasoning = rejected["reasoning"]
                    
                    pair = ActionPreferencePair(
                        goal=goal,
                        prev_actions=prev_actions,
                        prev_observations=prev_observations,
                        chosen_reasoning=chosen_reasoning,
                        chosen_action=chosen_action,
                        rejected_reasoning=rejected_reasoning,
                        rejected_action=rejected_action
                    )
                    pairs.append(pair)
        
        return pairs
    
    def generate_state_preference_pairs(self, successful_trajectory, all_executed_actions):
        """
        Generate state preference pairs from a successful trajectory
        
        Args:
            successful_trajectory: A successful trajectory
            all_executed_actions: Dictionary mapping (state_image, action) to 
                                 outcomes of all executed actions
            
        Returns:
            List of state preference pairs
        """
        pairs = []
        
        for i in range(len(successful_trajectory.steps) - 1):
            current_step = successful_trajectory.steps[i]
            next_step = successful_trajectory.steps[i+1]
            
            # Current state
            current_state_image = current_step.state_image
            current_state_desc = current_step.state_desc
            
            # Action taken
            action = current_step.action + " " + current_step.object
            
            # Chosen next state (from successful trajectory)
            chosen_next_state = next_step.state_desc
            
            # Get alternative next states from other actions
            state_action_key = (current_state_image, i)
            if state_action_key in all_executed_actions:
                alt_actions = [
                    a for a in all_executed_actions[state_action_key]
                    if a["action"] != action and a["success"]
                ]
                
                for alt_action in alt_actions:
                    rejected_next_state = alt_action["next_state_desc"]
                    
                    pair = StatePreferencePair(
                        current_state_image=current_state_image,
                        current_state_desc=current_state_desc,
                        action=action,
                        chosen_next_state=chosen_next_state,
                        rejected_next_state=rejected_next_state
                    )
                    pairs.append(pair)
        
        return pairs

