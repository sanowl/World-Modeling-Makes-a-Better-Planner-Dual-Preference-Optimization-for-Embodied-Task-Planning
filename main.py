class D2POTrainer:
    def __init__(
        self, 
        model, 
        ref_model,
        tokenizer,
        action_preference_pairs,
        state_preference_pairs,
        beta=0.1,
        lambda_weight=1.0,
        lr=5e-7,
        batch_size=32,
        num_epochs=1,
        device="cuda"
    ):
        """
        Initialize the D²PO trainer
        
        Args:
            model: The model to be trained
            ref_model: Reference model (SFT model)
            tokenizer: Tokenizer
            action_preference_pairs: List of action preference pairs
            state_preference_pairs: List of state preference pairs
            beta: DPO beta parameter
            lambda_weight: Weight for state prediction loss
            lr: Learning rate
            batch_size: Batch size
            num_epochs: Number of epochs
            device: Device to train on
        """
        self.model = model.to(device)
        self.ref_model = ref_model.to(device)
        self.tokenizer = tokenizer
        self.beta = beta
        self.lambda_weight = lambda_weight
        self.device = device
        
        # Freeze reference model
        for param in self.ref_model.parameters():
            param.requires_grad = False
            
        # Create datasets
        self.action_dataset = ActionPreferenceDataset(action_preference_pairs, tokenizer)
        self.state_dataset = StatePreferenceDataset(state_preference_pairs, tokenizer)
        
        # Create dataloaders
        self.action_dataloader = DataLoader(
            self.action_dataset, 
            batch_size=batch_size, 
            shuffle=True
        )
        
        self.state_dataloader = DataLoader(
            self.state_dataset, 
            batch_size=batch_size, 
            shuffle=True
        )
        
        # Optimizer
        self.optimizer = optim.Adam(self.model.parameters(), lr=lr)
        
        self.num_epochs = num_epochs
    
    def compute_logps(self, input_ids, attention_mask, images=None):
        """Compute log probabilities for sequences"""
        with torch.no_grad():
            outputs = self.model(
                input_ids=input_ids,
                attention_mask=attention_mask,
                images=images
            )
            
        logits = outputs.logits
        
        # Shift logits and input_ids for next token prediction
        shift_logits = logits[:, :-1, :].contiguous()
        shift_labels = input_ids[:, 1:].contiguous()
        
        # Compute log probs
        log_probs = F.log_softmax(shift_logits, dim=-1)
        token_log_probs = log_probs.gather(-1, shift_labels.unsqueeze(-1)).squeeze(-1)
        
        # Mask padding tokens
        mask = attention_mask[:, 1:].contiguous()
        token_log_probs = token_log_probs * mask
        
        # Sum log probs
        sequence_log_probs = token_log_probs.sum(dim=-1) / mask.sum(dim=-1)
        
        return sequence_log_probs
    
    def dpo_loss(self, chosen_logps, chosen_ref_logps, rejected_logps, rejected_ref_logps):
        """Compute DPO loss"""
        logits = self.beta * (chosen_logps - chosen_ref_logps - (rejected_logps - rejected_ref_logps))
        losses = -F.logsigmoid(logits)
        return losses.mean()
    
    def train_epoch(self, epoch):
        """Train for one epoch"""
        self.model.train()
        total_loss = 0
        action_loss = 0
        state_loss = 0
        
        # Determine number of iterations (max of action and state dataloaders)
        num_batches = max(len(self.action_dataloader), len(self.state_dataloader))
        
        # Create iterators
        action_iter = iter(self.action_dataloader)
        state_iter = iter(self.state_dataloader)
        
        # Track processed data
        action_batches_processed = 0
        state_batches_processed = 0
        
        for i in range(num_batches):
            self.optimizer.zero_grad()
            batch_loss = 0
            
            # Process action batch if available
            if action_batches_processed < len(self.action_dataloader):
                try:
                    action_batch = next(action_iter)
                    action_batches_processed += 1
                    
                    # Move to device
                    chosen_input_ids = action_batch["chosen_input_ids"].to(self.device)
                    chosen_attention_mask = action_batch["chosen_attention_mask"].to(self.device)
                    rejected_input_ids = action_batch["rejected_input_ids"].to(self.device)
                    rejected_attention_mask = action_batch["rejected_attention_mask"].to(self.device)
                    
                    # Compute log probs for model
                    chosen_logps = self.compute_logps(
                        chosen_input_ids, 
                        chosen_attention_mask
                    )
                    
                    rejected_logps = self.compute_logps(
                        rejected_input_ids, 
                        rejected_attention_mask
                    )
                    
                    # Compute log probs for reference model
                    with torch.no_grad():
                        chosen_ref_logps = self.compute_logps(
                            chosen_input_ids, 
                            chosen_attention_mask
                        )
                        
                        rejected_ref_logps = self.compute_logps(
                            rejected_input_ids, 
                            rejected_attention_mask
                        )
                    
                    # Compute action selection loss
                    batch_action_loss = self.dpo_loss(
                        chosen_logps, 
                        chosen_ref_logps, 
                        rejected_logps, 
                        rejected_ref_logps
                    )
                    
                    action_loss += batch_action_loss.item()
                    batch_loss += batch_action_loss
                    
                except StopIteration:
                    pass
            
            # Process state batch if available
            if state_batches_processed < len(self.state_dataloader):
                try:
                    state_batch = next(state_iter)
                    state_batches_processed += 1
                    
                    # Move to device
                    chosen_input_ids = state_batch["chosen_input_ids"].to(self.device)
                    chosen_attention_mask = state_batch["chosen_attention_mask"].to(self.device)
                    rejected_input_ids = state_batch["rejected_input_ids"].to(self.device)
                    rejected_attention_mask = state_batch["rejected_attention_mask"].to(self.device)
                    
                    # For VLMs, we would also process images
                    # This is simplified
                    
                    # Compute log probs for model
                    chosen_logps = self.compute_logps(
                        chosen_input_ids, 
                        chosen_attention_mask
                    )
                    
                    rejected_logps = self.compute_logps(
                        rejected_input_ids, 
                        rejected_attention_mask
                    )
                    
                    # Compute log probs for reference model
                    with torch.no_grad():
                        chosen_ref_logps = self.compute_logps(
                            chosen_input_ids, 
                            chosen_attention_mask
                        )
                        
                        rejected_ref_logps = self.compute_logps(
                            rejected_input_ids, 
                            rejected_attention_mask
                        )
                    
                    # Compute state prediction loss
                    batch_state_loss = self.dpo_loss(
                        chosen_logps, 
                        chosen_ref_logps, 
                        rejected_logps, 
                        rejected_ref_logps
                    )
                    
                    # Apply lambda weight
                    weighted_state_loss = self.lambda_weight * batch_state_loss
                    
                    state_loss += batch_state_loss.item()
                    batch_loss += weighted_state_loss
                    
                except StopIteration:
                    pass
            
            # Backprop combined loss
            if batch_loss > 0:
                batch_loss.backward()
                self.optimizer.step()
                total_loss += batch_loss.item()
            
        # Compute average losses
        avg_total_loss = total_loss / num_batches
        avg_action_loss = action_loss / action_batches_processed if action_batches_processed > 0 else 0
        avg_state_loss = state_loss / state_batches_processed if state_batches_processed > 0 else 0
        
        return avg_total_loss, avg_action_loss, avg_state_loss
    
    def train(self):
        """Train the model for multiple epochs"""
        best_loss = float('inf')
        
        for epoch in range(self.num_epochs):
            avg_loss, action_loss, state_loss = self.train_epoch(epoch)
            
            print(f"Epoch {epoch+1}/{self.num_epochs}, "
                  f"Total Loss: {avg_loss:.4f}, "
                  f"Action Loss: {action_loss:.4f}, "
                  f"State Loss: {state_loss:.4f}")
            
            # Save best model
            if avg_loss < best_loss:
                best_loss = avg_loss
                torch.save(self.model.state_dict(), "best_d2po_model.pt")
                
        print("Training complete!")
